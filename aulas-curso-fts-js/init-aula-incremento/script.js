/* Explicação sobre operadores de Incremento e Decremento */

/* "x++" sempre vai somar +1 */
let x = 1;
console.log(x++); /* Vai mostrar o valor antes de somar */
console.log(x); /* Vai mostrar o valor somado */

/* O mesmo vale para a subtração */
let y = 1;
console.log(y--); /* Vai mostrar o valor antes de subtrair */
console.log(y); /* Vai mostrar o valor subtraido */

/* Aqui vai somar um valor a variavel */
let a = 1;
console.log((a += 1)); /* Faz a soma e ja mostra o valor */

/* Aqui vai subtrair o valor */
let b = 1;
console.log((b -= 1)); /* Faz a subtração e ja mostra o valor */

/* Funciona também com outros operadores (*, /, etc.) */
